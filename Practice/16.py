numbers = [10, 20, 30, 40, 50, 60]

print(numbers[1:4])   # [20, 30, 40] ➔ from index 1 to 3
print(numbers[:3])    # [10, 20, 30] ➔ start from beginning
print(numbers[3:])    # [40, 50, 60] ➔ till end
print(numbers[::2])   # [10, 30, 50] ➔ every 2nd item
print(numbers[::-1])  # [60, 50, 40, 30, 20, 10] ➔ reversed
