# Write a Python script that:

# Takes two integers as input from the user.
# Prints their sum, product, and integer division result.

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

sumOfTwoNumbers = num1 + num2
print("sumOfTwoNumbers", sumOfTwoNumbers)

multifyOfTwoNumbers = num1 * num2
print("multifyOfTwoNumbers", multifyOfTwoNumbers)

divisionOfTwoNumbers = num1 // num2
print("divisionOfTwoNumbers", divisionOfTwoNumbers)